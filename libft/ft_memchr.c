#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*a;

	a = (unsigned char *)s;
	while (*a != (unsigned char)c && --n > 0)
		a++;
	if (n == 0)
		return (NULL);
	return ((void *)a);
}
